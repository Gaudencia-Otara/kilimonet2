                <h1>PARASITES</h1>
                <p>Parasites can be classified into categories name:
                  <ul>
                   <li> external parasites e.g ticks </li>
                   <li> internal parasites e.g liverfluke </li>
                  </ul>
                    
                    <h3>SYMPTOMS OF AN ANIMAL INFECTED BY PARASITES </h3>
                    <ol>
                     <li>rough coat</li>
                    <li> coughing</li>
                   <li> Stomach cramps and pain </li>
                    <li> Nausea or vomiting</li>
                    <li> Dehydration</li>
                    <li>Weight loss</li>
                   <li>Swollen lymph nodes</li>
                    <li>Digestive problems including unexplained constipation, diarrhoea or persistent gas </li>
                     <li>Skin issues such as rashes, eczema, hives, and itching</li>
