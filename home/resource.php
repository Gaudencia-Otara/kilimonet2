                <h3>Fodder and Pastures Management </h3>
                <p>kenya arid and semi arid land such kajiado county makes 80% of kenya landmass and home to a third 
                    of kenya population but livelihood is under threat of climate change. 
                    Climate change has led to scarcity of pasture and fodder .as a result 
                    pastoralism is severely affected .this can be can be mitigated 
                    through initiation of programs such as :<br></p>
                    <ul>
                      <li>1.Participatory rangeland management(PRM).</li>
                      <p> PRM encourages locals to take more active roles in land 
                        and resources management where by the land is map to identity key resources.</p>
                        <li>2.practicing rotational grazing
                         <p>In rotational grazing the grazing field has to be fence then subarticleide the field into small portions </p> 
           <li>3.Keeping few number of animals to control overgrazing </li> 
           <p>This help to ensure sufficient feeds are available throughout the season</p>
